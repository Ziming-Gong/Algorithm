    //need n extra space
    public static boolean isPalindrome1(Node head){
        if(head == null){
            return true;
        }
        Stack<Node> stack = new Stack<>();
        Node cur = head;
        while( cur != null){
            stack.push(cur);
            cur = cur.next;
        }
        cur = head;
        while( cur != null){
            if( cur.val != stack.peek().val){
                return false;
            }
            cur = cur.next;
            stack.pop();
        }
        return true;
    }

    //no extra space
    public static boolean isPalindrome3(Node head){
        if( head == null || head.next == null){
            return true;
        }
        //find mid
        Node n2 = head;
        Node n1 = head;
        while( n2.next != null && n2.next.next != null){
            n2 = n2.next.next;
            n1 = n1.next;
        }
        boolean ans = true;

        n2 = n1.next;
        n1.next = null;
        Node n3 = null;
        while( n2 != null){
            n3 = n2.next;
            n2.next = n1;
            n1 = n2;
            n2 = n3;
        }
        n3 = n1;
        n2 = head;
        while(n1 != null & n2 != null){//n2 != null?
            if (n1.val != n2.val) {
                ans = false;
                break;
            }
            n1 = n1.next;
            n2 = n2.next;
        }
        //还原链表
        n1 = n3.next;
        n3.next = null;
        while( n1 != null){
            n2 = n1.next;
            n1.next = n3;
            n3 = n1;
            n1 = n2;
        }
        return ans;
    }
    // need n/2 extra space
    public static boolean isPalindrome2(Node head) {
        if (head == null || head.next == null) {
            return true;
        }
        Node right = head.next;
        Node cur = head;
        while (cur.next != null && cur.next.next != null) {
            right = right.next;
            cur = cur.next.next;
        }
        Stack<Node> stack = new Stack<Node>();
        while (right != null) {
            stack.push(right);
            right = right.next;
        }
        while (!stack.isEmpty()) {
            if (head.val != stack.pop().val) {
                return false;
            }
            head = head.next;
        }
        return true;
    }
    public static void printLinkedList(Node node) {
        System.out.print("Linked List: ");
        while (node != null) {
            System.out.print(node.val + " ");
            node = node.next;
        }
        System.out.println();
    }

